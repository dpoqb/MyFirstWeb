         <p><h1><small class="text-success">知</small></h1>识管理与创新研究中心是一所多功能性实验室，覆盖管理科学与工程、数据分析、网络舆情、Java高级语言、微信分析、科学仿真、网站建设与维护等领域。秉承湖北工业大学“厚德博学，求实创新”的校训，知识管理与创新中心旨在为学校的科学文化建设提供具有创新能力的人才。</p>
